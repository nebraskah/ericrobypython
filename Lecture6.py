"""
Booleans & Operators
"""

is_human = True
love_fashion = False
donut  = 'Donut'
fav_number = 5
print(type(donut))
print(type(fav_number))
print(type(love_fashion))
print(type(is_human))


"""
Comparison Operators
"""
print(2 == 3)
print(2 != 3)
print(2 >= 3)
print(2 <= 3)


"""
Logical Operators
"""
print(2 >= 3 and 4 < 1)
print(2 >= 3 or 4 < 1)
print(2 <= 3 or 4 < 1)

print(not(4 == 7))
print(not(7 == 7))
